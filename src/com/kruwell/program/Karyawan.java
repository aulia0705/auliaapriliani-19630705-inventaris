    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        try{
            String sql = "INSERT INTO Data_Karyawan VALUES ('"+txtid.getText()+"','"+txtnama.getText()+"','"+txtalamat.getText()+"','"+cbjabatan.getSelectedItem()+"','"+txtemail.getText()+"')";
            java.sql.Connection conn = (Connection)Inventaris.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil..");
            tampilkan_data();
            kosongkan_form();
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btsimpanActionPerformed

    private void tabelkaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelkaryawanMouseClicked
        int baris = tabelkaryawan.rowAtPoint(evt.getPoint());
        String id = tabelkaryawan.getValueAt(baris, 1).toString();
        txtid.setText(id);
        
        String nama = tabelkaryawan.getValueAt(baris, 2).toString();
        txtnama.setText(nama);
        
        String alamat = tabelkaryawan.getValueAt(baris, 3).toString();
        txtalamat.setText(alamat);
        
        String jab = tabelkaryawan.getValueAt(baris, 4).toString();
        cbjabatan.setSelectedItem(jab);
        
        String email = tabelkaryawan.getValueAt(baris, 5).toString();
        txtemail.setText(email);
    }//GEN-LAST:event_tabelkaryawanMouseClicked

    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
        String sql = "UPDATE data_karyawan SET id_karyawan=?,nama_karyawan=?,alamat=?,jabatan=?,email=?";
        try {
            java.sql.Connection conn = (Connection)Inventaris.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, txtid.getText());
            pstm.setString(2, txtnama.getText());
            pstm.setString(3, txtalamat.getText());
            pstm.setString(4, cbjabatan.getSelectedItem().toString());
            pstm.setString(5, txtemail.getText());

            pstm.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil di ubah");
            kosongkan_form();
            txtid.requestFocus();
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal di ubah"+e);
        }
        tampilkan_data();
    }//GEN-LAST:event_bteditActionPerformed

