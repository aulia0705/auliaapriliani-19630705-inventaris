    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Id Karyawan");
        model.addColumn("Nama Karyawan");
        model.addColumn("Alamat");
        model.addColumn("Jabatan");
        model.addColumn("Email");
        
        try {
           int no = 1;
           String sql = "SELECT * FROM Data_Karyawan";
           java.sql.Connection conn=(Connection)Inventaris.configDB();
           java.sql.Statement stm= conn.createStatement();
           java.sql.ResultSet res = stm.executeQuery(sql);
           
           while (res.next()) {
               model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
               
           }
           
           tabelkaryawan.setModel(model);
        }catch (SQLException e) {
            System.out.println("Error :" + e.getMessage());
        }
    }

