    private void bttambahdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttambahdataActionPerformed
       try {
         String path ="./src/Report/rbarang.jasper";
           HashMap parameter =  new HashMap();
           JasperPrint print = JasperFillManager.fillReport(path, parameter,Inventaris.configDB());
           JasperViewer.viewReport(print, false);
       } catch (JRException ex){
           JOptionPane.showMessageDialog(rootPane, "Dokumen tidak ada dokumen"+ex);
        
       } catch (SQLException ex) {
         Logger.getLogger(Barang.class.getName()).log(Level.SEVERE, null, ex);
     }
    }//GEN-LAST:event_bttambahdataActionPerformed

    private void tabelbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelbarangMouseClicked
        int baris = tabelbarang.rowAtPoint(evt.getPoint());
        String no = tabelbarang.getValueAt(baris, 1).toString();
        txtno.setText(no);
        
        String nama = tabelbarang.getValueAt(baris, 2).toString();
        txtnamabarang.setText(nama);
        
        String jenis = tabelbarang.getValueAt(baris, 3).toString();
        txtjenis.setText(jenis);
        
        String jumlah = tabelbarang.getValueAt(baris, 4).toString();
        txtjumlah.setText(jumlah);
    }//GEN-LAST:event_tabelbarangMouseClicked

