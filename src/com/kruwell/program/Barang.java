    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
        try{
            String sql = "DELETE FROM Data_Barang WHERE no_barang='"+txtno.getText()+"'";
            java.sql.Connection conn = (Connection)Inventaris.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Hapus Data Berhasil...");
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        tampilkan_data();
        kosongkan_form();
    }//GEN-LAST:event_bthapusActionPerformed

    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        try{
            String sql = "INSERT INTO Data_Barang VALUES ('"+txtno.getText()+"','"+txtnamabarang.getText()+"','"+txtjenis.getText()+"','"+txtjumlah.getText()+"')";
            java.sql.Connection conn = (Connection)Inventaris.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil..");
            tampilkan_data();
            kosongkan_form();
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btsimpanActionPerformed

    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
       String sql = "UPDATE data_barang SET no_barang=?,nama_barang=?,jenis_barang=?,jumlah=?";
        try {
            java.sql.Connection conn = (Connection)Inventaris.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, txtno.getText());
            pstm.setString(2, txtnamabarang.getText());
            pstm.setString(3, txtjenis.getText());
            pstm.setString(4, txtjumlah.getText());

            pstm.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil di ubah");
            kosongkan_form();
            txtno.requestFocus();
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal di ubah"+e);
        }
        tampilkan_data();
    }//GEN-LAST:event_bteditActionPerformed

